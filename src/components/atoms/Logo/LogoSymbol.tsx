export function LogoSymbol({ hovered }: { hovered: boolean }) {
  return (
    <svg
      viewBox="-8 -8 116 108.8"
      fill="currentColor"
      className={styles.symbol}
      style={{ overflow: "visible" }}
      aria-hidden="true"
    >
      {DOTS.map(([cx, cy, r], i) => {
        const dx = cx - CX;
        const dy = cy - CY;
        const dist = Math.sqrt(dx * dx + dy * dy);
        const tx = dx * EXPAND;
        const ty = dy * EXPAND;
        const delay = Math.round(dist * 3);

        return (
          <circle
            key={i}
            cx={cx}
            cy={cy}
            r={r}
            style={{
              transition: `transform 0.5s cubic-bezier(0.34, 1.56, 0.64, 1) ${delay}ms`,
              transform: hovered
                ? `translate(${tx.toFixed(2)}px, ${ty.toFixed(2)}px)`
                : "translate(0, 0)",
            }}
          />
        );
      })}
    </svg>
  );
}
